//      switch case statement 

const month = 4

switch(month)
{
    case 1:
        console.log("jan")
        break;
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("mar")
        break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("may")
        break;

    default: 
    console.log("value not match");
    break;
}
// break statement used to stop the control flow of the code 

